package br.com.projetoMVC.model;

public class Produto {

	int id;
	String descrição;
	
	
	public Produto() {
		super();
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescrição() {
		return descrição;
	}
	public void setDescrição(String descrição) {
		this.descrição = descrição;
	}
	
	
	
}
